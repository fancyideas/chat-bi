
-- 空气质量数据表
CREATE TABLE DW_WEATHER_AIR(
    ID BIGINT NOT NULL  COMMENT '主键',
    AREA_ID VARCHAR(10)   COMMENT '城市站点号，该字段与DW_WEATHER_AREA表AREA_ID字段关联',
    CITY_CODE VARCHAR(100)   COMMENT '城市代码',
    CITY VARCHAR(50)   COMMENT '城市',
    CITY_EN VARCHAR(50)   COMMENT '城市（英文）',
    PM25 VARCHAR(10)   COMMENT 'PM2.5;整数，单位微克每立方米，如99',
    AQI VARCHAR(10)   COMMENT 'AQI;整数，单位微克每立方米，如99',
    NO2 VARCHAR(10)   COMMENT 'NO2;整数，单位微克每立方米，如36',
    O3 VARCHAR(10)   COMMENT 'O3;整数，单位微克每立方米，如4',
    PM10 VARCHAR(10)   COMMENT 'PM10;整数，单位微克每立方米，如3',
    SO2 VARCHAR(10)   COMMENT 'SO2;整数，单位微克每立方米，如3',
    CO VARCHAR(10)   COMMENT 'CO;一位小数，单位毫克每立方米，如1.2',
    TIME VARCHAR(14)   COMMENT '预报更新时间;预报更新时间,201601150800',
    STATION_CODE VARCHAR(100)   COMMENT '机场代码',
    DELETE_FLAG INT   DEFAULT  0 COMMENT '删除标记',
    CREATED_TIME TIMESTAMP   COMMENT '创建时间',
    UPDATED_TIME TIMESTAMP   COMMENT '更新时间'，
    PRIMARY KEY (ID)
);

COMMENT ON TABLE DW_WEATHER_AIR IS 'DW层空气质量数据表';

