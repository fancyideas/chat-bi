-- 天气城市站点表
CREATE TABLE DW_WEATHER_AREA(
    AREA_ID VARCHAR2(20)   NOT NULL   COMMENT '站点ID',
    AREA_NAME VARCHAR2(64)   COMMENT '站点中文名称',
    AREA_FLIN VARCHAR2(1)   COMMENT '国际国内',
    CITY_NAMECHN VARCHAR2(64)   COMMENT '城市中文名称（关联城市表中文描述）',
    CITY_NAMEENG VARCHAR2(64)   COMMENT '城市英文名称',
    CITY_CODE VARCHAR2(4)   COMMENT '城市代码',
    COUNTRY_CODE VARCHAR2(4)   COMMENT '国家代码',
    AIRPORT_CODE VARCHAR2(64)   COMMENT '机场代码,一个城市多个机场时机场三字码以|分隔，如SHE|PEK',
    DELETE_FLAG INTEGER   DEFAULT  0 COMMENT '逻辑删除标识',
    CREATED_BY VARCHAR2(32)   DEFAULT  'SYSTEM' COMMENT '创建人',
    CREATED_TIME TIMESTAMP   DEFAULT  CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATED_BY VARCHAR2(32)   DEFAULT  'SYSTEM' COMMENT '更新人',
    UPDATED_TIME TIMESTAMP   DEFAULT  CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (AREA_ID)
);

COMMENT ON TABLE DW_WEATHER_AREA IS '天气城市站点表';